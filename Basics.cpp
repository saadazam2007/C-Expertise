#include<iostream>
using namespace std;
int main(){
    //Increment operator
    //Prefix Increment operator
    int x;
    x = 4;
    cout << ++x << endl;
    //Postfix Increment operator
    int y;
    y=5;
    cout << y++ << endl;

    //Decrement operator
    //Prefix Decrement operator
    int a;
    a = 12;
    cout << --a << endl;
    //Postfix Decrement operator
    int b;
    b = 10;
    cout << b-- << endl;
    
}